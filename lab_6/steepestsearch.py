def sdsearch(f, df, x0, tol):
    # SDSEARCH searches for minimum using steepest descent method
    # 	answer_ = sdsearch(f, df, x0, tol)
    #   INPUT ARGUMENTS
    #   f  - objective function
    #   df - gradient
    # 	x0 - start point
    # 	tol - set for bot range and function value
    #   OUTPUT ARGUMENTS
    #   answer_ = [xmin, fmin, neval, coords]
    # 	xmin is a function minimizer
    # 	fmin = f(xmin)
    # 	neval - number of function evaluations
    #   coords - array of statistics

    kmax = 1000
    x = x0
    coords = []
    coords.append(x)
    neval = 0
    k = 0
    deltaX = np.inf

    while (norm(deltaX) >= tol) and (k < kmax):
        grad = df(x)
        neval += 1

        f1dim = lambda alpha: f(x - alpha * grad)

        interval = [0.0, 1.0]
        alpha_opt, _, neval_1d = goldensectionsearch(f1dim, interval, tol)
        neval += neval_1d

        x_new = x - alpha_opt * grad
        deltaX = x_new - x

        coords.append(x_new)
        x = x_new
        k += 1

    xmin = x
    fmin = f(xmin)

    answer_ = [xmin, fmin, neval, coords]
    return answer_
